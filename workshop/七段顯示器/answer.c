#include <stdio.h>

int main() {
    char segments[10][3][3] = {
        {
            {' ', '_', ' '},
            {'|', ' ', '|'},
            {'|', '_', '|'}
        },  // 0

        {
            {' ', ' ', ' '},
            {' ', ' ', '|'},
            {' ', ' ', '|'}
        },  // 1

        {
            {' ', '_', ' '},
            {' ', '_', '|'},
            {'|', '_', ' '}
        },  // 2

        {
            {' ', '_', ' '},
            {' ', '_', '|'},
            {' ', '_', '|'}
        },  // 3

        {
            {' ', ' ', ' '},
            {'|', '_', '|'},
            {' ', ' ', '|'}
        },  // 4

        {
            {' ', '_', ' '},
            {'|', '_', ' '},
            {' ', '_', '|'}
        },  // 5

        {
            {' ', '_', ' '},
            {'|', '_', ' '},
            {'|', '_', '|'}
        },  // 6

        {
            {' ', '_', ' '},
            {' ', ' ', '|'},
            {' ', ' ', '|'}
        },  // 7

        {
            {' ', '_', ' '},
            {'|', '_', '|'},
            {'|', '_', '|'}
        },  // 8

        {
            {' ', '_', ' '},
            {'|', '_', '|'},
            {' ', '_', '|'}
        }   // 9
    };

    int number;
    printf("Enter a number (0-9): ");
    scanf("%d", &number);

    if (number < 0 || number > 9) {
        printf("Invalid number!\n");
        return 0;
    }

    printf("Number %d:\n", number);
    for (int row = 0; row < 3; row++) {
        for (int col = 0; col < 3; col++) {
            printf("%c", segments[number][row][col]);
        }
        printf("\n");
    }

    return 0;
}

